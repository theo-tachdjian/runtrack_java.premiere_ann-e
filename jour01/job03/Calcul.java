class Calcul {
	
	public static void main(String[] args) {
		// "nombre" vaut 7 et est affiché
		int nombre = 7;
		System.out.println(nombre);
		// On ajoute 3 à "nombre" puis on l'affiche
		nombre += 3;
		System.out.println(nombre);
		// Puis on multiplie "nombre" par 5 et on l'affiche
		nombre *= 5;
		System.out.println(nombre);
		
	}
	
}